struct folder_t
{
    uint32_t file_name_offset;
    uint16_t first_subfolder_index;
    uint16_t last_subfolder_index;
    uint16_t first_filename_index;
    uint16_t last_filename_index;
};

std::ostream& operator<< (std::ostream& stream, const folder_t& folder)
{
    stream << "folder \n";
    stream << "{\n";
    stream << "    file_name_offset      = " << folder.file_name_offset << "\n";
    stream << "    first_subfolder_index = " << folder.first_subfolder_index << "\n";
    stream << "    last_subfolder_index  = " << folder.last_subfolder_index << "\n";
    stream << "    first_filename_index  = " << folder.first_filename_index << "\n";
    stream << "    last_filename_index   = " << folder.last_filename_index << "\n";
    stream << "}\n";
    return stream;
}

void safe_read(std::istream& stream, char* destination, std::size_t count)
{
    std::size_t bytes_to_read = count;
    std::size_t read_bytes = 0;

    while (bytes_to_read)
    {
        if (!stream.good())
        {
            throw std::runtime_error("bang");
            //bool fail = stream.fail();
            //bool bad = stream.bad();
            //bool eof = stream.eof();
            //__debugbreak();
        }
        std::size_t read = stream.read(destination + read_bytes, bytes_to_read).gcount();

        bytes_to_read -= read;
        read_bytes += read;
    }
}

template<typename T>
std::vector<T> read_list(std::istream& stream, std::size_t count, std::size_t filepos)
{
    std::vector<T> list(count);

    stream.seekg(filepos, std::ios::beg);

    for (std::size_t i = 0; i < count; ++i)
    {
        //auto streampos = stream.tellg(); //with this uncommented (in MSVC) it works for some reason
        safe_read(stream, reinterpret_cast<char*>(&list[i]), sizeof(T));
    }
    return list;
}

