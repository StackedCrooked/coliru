#include <iostream>
using namespace std;
//-----------------------------------------------------------------------------------------//
int process(int & in)
{
    cout << "processing int::" << in << endl;
}
//-----------------------------------------------------------------------------------------//
int process(const char * in)
{
    cout << "processing char::" << in << endl;
}
//-----------------------------------------------------------------------------------------//
int process(float & in)
{
    cout << "processing float::" << in << endl;
}
//-----------------------------------------------------------------------------------------//
int test()
{
    cout << "End of parameter pack" << endl;
}
//-----------------------------------------------------------------------------------------//
template <typename T, typename T1>
int test(T t, T1 t1)
{
    cout << "Processing last 2 parameters:"<< t << ":" << t1 << endl;
    process(t);
    process(t1);
    test();
}
//-----------------------------------------------------------------------------------------//
template <typename T, typename... Tail>
int test(T head, Tail... tail)
{
    cout << "Head:" << head << endl; 
	process(head);

	test(tail...);
}
//-----------------------------------------------------------------------------------------//
int main()
{
    test(1,"A", 2.56F, 12);
}
