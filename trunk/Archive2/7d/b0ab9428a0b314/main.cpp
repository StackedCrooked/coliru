#include <iostream>

using namespace std;

int main()
{
    int x = 3;
    int y = 7;
    int z = x + y;
    int w = x * y;
    int a = y / x;
    int b = x - y;
    int c = y % x;
    
    cout << "x = " << x << endl;
    cout << "y = " << y << endl;
    cout << "z = " << z << endl;
    cout << "w = " << w << endl;
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "c = " << c << endl;
    
    return 0;
}
